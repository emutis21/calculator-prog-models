  private void handleNegativeSignButton() {
    String currentText = casilla.getText();

    if (currentText.isEmpty()) {
      casilla.setText("-");
    } else {
      if (currentText.startsWith("-")) {
        casilla.setText(currentText.substring(1));
      } else {
        casilla.setText("-" + currentText);
      }
    }
  }

