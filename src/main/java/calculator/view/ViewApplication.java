  private void jButton21ActionPerformed(java.awt.event.ActionEvent evt) { //GEN-FIRST:event_jButton21ActionPerformed
    try {
      double result;
      double firstNumber = Double.parseDouble(this.firstNumber);
      double secondNumber = Double.parseDouble(casilla.getText());

      Calculator calculator = new Calculator();

      switch (this.operator) {
        case "+" -> calculator.setOperation(new Addition());
        case "-" -> calculator.setOperation(new Subtraction());
        case "*" -> calculator.setOperation(new Multiplication());
        case "/" -> calculator.setOperation(new Division());
      }

      if (this.operator.equals("/") && secondNumber == 0) {
        if (firstNumber == 0) {
          casilla.setText("No se puede dividir entre cero");
        } else {
          casilla.setText("Error: Divisi√≥n por cero");
        }
      } else {
        result = calculator.calculate(firstNumber, secondNumber);
        casilla.setText(String.valueOf(result));
      }
    } catch (NumberFormatException | ArithmeticException e) {
      casilla.setText("Error");
    }
  } //GEN-LAST:event_jButton21ActionPerformed

